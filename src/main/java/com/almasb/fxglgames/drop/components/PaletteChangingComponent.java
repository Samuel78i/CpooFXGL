package com.almasb.fxglgames.drop.components;

import com.almasb.fxgl.core.math.FXGLMath;
import com.almasb.fxgl.entity.component.Component;
import com.almasb.fxgl.texture.Texture;
import javafx.geometry.Rectangle2D;

/**
 * @author Almas Baimagambetov (almaslvl@gmail.com)
 */
public class PaletteChangingComponent extends Component {

    private Texture texture;

    private boolean makeMeBigger = false;

    public PaletteChangingComponent(Texture texture) {
        this.texture = texture;
    }

    @Override
    public void onAdded() {
        entity.getViewComponent().addChild(texture);
    }

    @Override
    public void onUpdate(double tpf) {
        if(makeMeBigger) {
            double width = texture.getWidth() + 2;
            double height = texture.getHeight()+ 4;
            texture.setViewport(new Rectangle2D(0, 0, width, height));
        }
    }

}
